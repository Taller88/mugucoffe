public class OrderNumVO extends DTO {

	private String orderNum;

	public OrderNumVO() {}
	
	/**
	 * title       OrderNumVO
	 * description OrderNumVO 생성자로 정보를 입력받는 인자 설정 메서드
	 * @param 	   orderNum
	 */
	public OrderNumVO(String orderNum) {
		super();
		this.orderNum = orderNum;
	}

	/**
	 * @return the orderNum
	 */
	public String getOrderNum() {
		return orderNum;
	}

	/**
	 * @param orderNum the orderNum to set
	 */
	public void setOrderNum(String orderNum) {
		this.orderNum = orderNum;
	}
	
	/**
	 * title       toString
	 * description OrderNumVO toString
	 * @return 		orderNum, super.toString()
	 */
	@Override
	public String toString() {
		return "OrderNumVO [orderNum=" + orderNum + ", toString()=" + super.toString() + "]";
	}
	
}
