public class StoreVO extends DTO {

	private String storeName;
	
	private String storeArea;
	
	private String storeCellPhone;

	public StoreVO() {}
	
	/**
	 * title       StoreVO
	 * description StoreVO 생성자로 정보를 입력받는 인자 설정 메서드
	 * @param 		storeName, storeArea, storeCellPhone
	 */
	public StoreVO(String storeName, String storeArea, String storeCellPhone) {
		super();
		this.storeName = storeName;
		this.storeArea = storeArea;
		this.storeCellPhone = storeCellPhone;
	}

	/**
	 * @return the storeName
	 */
	public String getStoreName() {
		return storeName;
	}

	/**
	 * @param storeName the storeName to set
	 */
	public void setStoreName(String storeName) {
		this.storeName = storeName;
	}

	/**
	 * @return the storeArea
	 */
	public String getStoreArea() {
		return storeArea;
	}

	/**
	 * @param storeArea the storeArea to set
	 */
	public void setStoreArea(String storeArea) {
		this.storeArea = storeArea;
	}

	/**
	 * @return the storeCellPhone
	 */
	public String getStoreCellPhone() {
		return storeCellPhone;
	}

	/**
	 * @param storeCellPhone the storeCellPhone to set
	 */
	public void setStoreCellPhone(String storeCellPhone) {
		this.storeCellPhone = storeCellPhone;
	}
	
	/**
	 * title       toString
	 * description StoreVO toString
	 * @return 	   storeName, storeArea, storeCellPhone, super.toString()
	 */
	@Override
	public String toString() {
		return "StoreVO [storeName=" + storeName + ", storeArea=" + storeArea + ", storeCellPhone=" + storeCellPhone
				+ ", toString()=" + super.toString() + "]";
	}
	
}
