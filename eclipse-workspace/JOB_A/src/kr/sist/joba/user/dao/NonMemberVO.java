package kr.sist.joba.user.dao;

import data.DTO;

/**
 * <pre>
 * PackageName : kr.sist.joba.user.dao
 * ClassName : NonMemberVO.java
 * Description : 비회원 정보 입력 받는 VO 클래스
 *  ======Modification Information======
 *  생성일                  생성자                  수정내용
 *  ----------  --------   -------------------------------
 *  2019-11-26  이지은                  최초 생성
 *
 * </pre>
 * @since : 2019-11-26
 * @version : 1.0
 * @author : 쌍용교육센터 E반 1조 JOB_A
 */
public class NonMemberVO extends DTO {
	/**비회원 이름*/
	private String nonMemberName;
	/**비회원 비밀번호*/
	private String nonMemberPw;
	/**비회원 휴대폰 번호*/
	private String nonMemberCellPhone;
	
	public NonMemberVO() {}
	
	/**
     * title       NonMemberVO
     * description 생성자로 입력받을 비회원 정보의 인자 설정
     * @param       nonMemberName, nonMemberPw, nonMemberCellPhone
     */
	public NonMemberVO(String nonMemberName, String nonMemberPw, String nonMemberCellPhone) {
		super();
		this.nonMemberName = nonMemberName;
		this.nonMemberPw = nonMemberPw;
		this.nonMemberCellPhone = nonMemberCellPhone;
	}

	/**
     * title       getNonMemberName
     * description nonMemberName 가져오기
     * @return      nonMemberName
     */
	public String getNonMemberName() {
		return nonMemberName;
	}

	/**
     * title       setNonMemberName
     * description nonMemberName 출력하기
     * @param       nonMemberName
     */
	public void setNonMemberName(String nonMemberName) {
		this.nonMemberName = nonMemberName;
	}

	/**
     * title       getNonMemberPw
     * description nonMemberPw 가져오기
     * @return      nonMemberPw
     */
	public String getNonMemberPw() {
		return nonMemberPw;
	}

	/**
     * title       setNonMemberPw
     * description nonMemberPw 출력하기
     * @param       nonMemberPw
     */
	public void setNonMemberPw(String nonMemberPw) {
		this.nonMemberPw = nonMemberPw;
	}

	/**
     * title       getNonMemberCellPhone
     * description nonMemberCellPhone 가져오기
     * @return      nonMemberCellPhone
     */
	public String getNonMemberCellPhone() {
		return nonMemberCellPhone;
	}

	/**
     * title       setNonMemberCellPhone
     * description nonMemberCellPhone 출력하기
     * @param       nonMemberCellPhone
     */
	public void setNonMemberCellPhone(String nonMemberCellPhone) {
		this.nonMemberCellPhone = nonMemberCellPhone;
	}

	@Override
	public String toString() {
		return "NonMemberVO [nonMemberName=" + nonMemberName + ", nonMemberPw=" + nonMemberPw + ", nonMemberCellPhone="
				+ nonMemberCellPhone + "]";
	}
	
}
